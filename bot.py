# –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å –∫–Ω–æ–ø–∫–∞–º–∏
def create_inline_keyboard(items, prefix=""):

    keyboard = types.InlineKeyboardMarkup(row_width=2)
    buttons = [types.InlineKeyboardButton(
        text=f"{ICONS.get(prefix, {}).get(item, '')} {item.capitalize()}",
        callback_data=f"{prefix}_{item}"
    ) for item in items]
    keyboard.add(*buttons)
    return keyboard

# –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ–≥–æ–¥—ã
def get_weather_description(weather_code):
    if weather_code in [800]:
        return "—è—Å–Ω–æ"
    elif weather_code in [801, 802]:
        return "–æ–±–ª–∞—á–Ω–æ"
    elif weather_code in [803, 804]:
        return "–ø–∞—Å–º—É—Ä–Ω–æ"
    elif weather_code in [300, 500]:
        return "–¥–æ–∂–¥—å"
    elif weather_code in [600, 601]:
        return "—Å–Ω–µ–≥"
    elif weather_code in [999]:
        return "—Ä–∞–∑–Ω–æ–æ–±—Ä–∞–∑–Ω–æ"
    else:
        return "–ø–æ–≥–æ–¥–Ω–∞—è –∞–Ω–æ–º–∞–ª–∏—è"

# –ø–æ–ª—É—á–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –æ –ø–æ–≥–æ–¥–µ —á–µ—Ä–µ–∑ openweathermap
def get_weather_data(city):
    url = f"http://api.openweathermap.org/data/2.5/weather?q={city}&appid={OWM_API_KEY}&units=metric&lang=ru"
    response = requests.get(url, timeout=10)
    data = response.json()

    if data['cod'] != 200:
        raise Exception(data['message'])

    return {
        'temp': data['main']['temp'],
        'weather_code': data['weather'][0]['id'],
        'description': data['weather'][0]['description'],
        'humidity': data['main']['humidity'],
        'wind': data['wind']['speed']
    }


